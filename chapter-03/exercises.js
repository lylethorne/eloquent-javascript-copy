// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
let count = 0;
for(let i = 0; i < string.length; i ++){
  if(string[i] === letter){
    count += count + 1;
  }
}
return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0;
  for(let i = 0; i < string.length; i ++){
    if(string[i] === 'B'){
      count += count + 1;
    }
  }
return count;
}

